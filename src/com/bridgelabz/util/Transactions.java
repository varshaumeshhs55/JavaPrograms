package com.bridgelabz.util;

public class Transactions {
	 private String date;
	 private String status;
	 
	 public Transactions() {
		 date=null;
		 status=null;
	 }
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	}



